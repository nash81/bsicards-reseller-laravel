  String t(String key) {
    final code = locale.languageCode;
    return _localizedValues[code]?[key] ??
        _localizedValues[fallbackLocale.languageCode]?[key] ??
        key;
  }

  static AppLocalizations of(BuildContext context) {
    final localizations = Localizations.of<AppLocalizations>(context, AppLocalizations);
    return localizations ?? AppLocalizations(fallbackLocale);
  }

  static LanguageOption languageOptionForCode(String code) {
    return languageOptions.firstWhere(
      (option) => option.locale.languageCode == code,
      orElse: () => languageOptions.first,
    );
  }

  static const LocalizationsDelegate<AppLocalizations> delegate = _AppLocalizationsDelegate();
}

class _AppLocalizationsDelegate extends LocalizationsDelegate<AppLocalizations> {
  const _AppLocalizationsDelegate();

  @override
  bool isSupported(Locale locale) {
    return AppLocalizations.supportedLocales
        .any((supported) => supported.languageCode == locale.languageCode);
  }

  @override
  Future<AppLocalizations> load(Locale locale) async {
    return AppLocalizations(locale);
  }

  @override
  bool shouldReload(covariant LocalizationsDelegate<AppLocalizations> old) => false;
}

extension LocalizationX on BuildContext {
  String tr(String key) => AppLocalizations.of(this).t(key);
